#include "rt.h"

void	init_zero_matrix_2x2(t_matrix_2x2 *matrix)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < 2)
	{
		while (j < 2)
		{
			matrix->m[i][j] = 0.0;
			j++;
		}
		j = 0;
		i++;
	}
}
